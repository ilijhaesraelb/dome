const allTranslations: Record<Locale, TranslationDict> = {
  en,
  es,
  fr,
  ht,
  ja,
  ur,
  hi,
  zh,
  de,
};

/**
 * Get a translation by key for a given locale.
 * Supports interpolation: t("portal.welcomeBack", { name: "John" })
 * Falls back to English, then to the key itself.
 */
export function getTranslation(
  locale: Locale,
  key: string,
  params?: Record<string, string | number>
): string {
  const dict = allTranslations[locale] || allTranslations.en;
  let value = dict[key] ?? allTranslations.en[key] ?? key;

  if (params) {
    for (const [k, v] of Object.entries(params)) {
      value = value.replace(`{${k}}`, String(v));
    }
  }

  return value;
}

export type { TranslationDict };
export { allTranslations };
