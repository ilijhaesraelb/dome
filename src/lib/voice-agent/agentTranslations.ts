/** Get the translated question for a field, falling back to English or fieldLabel */
export function getFieldQuestion(
  fieldKey: string,
  fieldLabel: string,
  fieldHelp: string | undefined,
  lang: AgentLang
): string {
  const translations = FIELD_Q[fieldKey];
  if (translations) return translations[lang] ?? translations["en"];
  if (fieldHelp) return `${fieldLabel}. ${fieldHelp}`;
  return fieldLabel;
}
