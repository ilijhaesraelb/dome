/**
 * Structured section definitions for each supported USCIS form.
 * Each section groups fields, provides help notes, and drives
 * the guided section-by-section workspace experience.
 *
 * GOLD STANDARD: I-130 is the reference implementation.
 */

export interface FieldHelp {
  what: string;
  example?: string;
  warning?: string;
  /** Where to find the answer on a real document (e.g. "Top right of your I-94"). */
  whereToFind?: string;
}

export interface FormFieldDef {
  key: string;
  label: string;
  placeholder: string;
  type?: "text" | "date" | "select" | "checkbox";
  required?: boolean;
  help?: FieldHelp;
  options?: string[];
  /** Evidence categories related to this field (shown in evidence panel) */
  evidenceHint?: string;
}

export interface FormSection {
  id: string;
  title: string;
  purpose: string;
  fields: FormFieldDef[];
}

